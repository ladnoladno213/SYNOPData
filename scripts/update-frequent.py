def main():
    print("=" * 70)
    print("ОБНОВЛЕНИЕ ЧАСТЫХ МЕТЕОСТАНЦИЙ")
    print("=" * 70)
    print(f"Всего станций: {len(FREQUENT_STATIONS)}")
    print(f"Период: {START_DATE} - {END_DATE}")
    print(f"Директория: {OUTPUT_DIR}")
    print("=" * 70)
    
    success = 0
    failed = 0
    
    for idx, wmo_id in enumerate(FREQUENT_STATIONS, 1):
        print(f"\n[{idx}/{len(FREQUENT_STATIONS)}] Обновление станции {wmo_id}...")
        
        try:
            result = download_station_archive(wmo_id, START_DATE, END_DATE)
            if result:
                success += 1
                print(f"✅ {wmo_id} - успешно")
            else:
                failed += 1
                print(f"❌ {wmo_id} - ошибка")
        except Exception as e:
            failed += 1
            print(f"❌ {wmo_id} - исключение: {e}")
    
    print("\n" + "=" * 70)
    print("ИТОГИ")
    print("=" * 70)
    print(f"Успешно: {success}")
    print(f"Ошибок: {failed}")
    print("=" * 70)
    
    # Сохраняем статистику
    stats = {
        'timestamp': datetime.now().isoformat(),
        'total': len(FREQUENT_STATIONS),
        'success': success,
        'failed': failed
    }
    
    stats_file = OUTPUT_DIR / 'update-stats.json'
    with open(stats_file, 'w') as f:
        json.dump(stats, f, indent=2)
    
    return 0 if failed == 0 else 1

if __name__ == '__main__':
    sys.exit(main())
